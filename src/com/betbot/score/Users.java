package com.betbot.score;

public class Users {
	private String username;
	private int score;
	private int tip1;
	private int tip2;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getTip1() {
		return tip1;
	}
	public void setTip1(int tip) {
		this.tip1 = tip;
	}
	public int getTip2() {
		return tip2;
	}
	public void setTip2(int tip2) {
		this.tip2 = tip2;
	}
}
